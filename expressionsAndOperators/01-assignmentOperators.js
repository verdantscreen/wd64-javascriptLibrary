// ASSIGNMENT OPERATOR:
let r = 5;
// our equal sign is the assignment

//ADDITION
r += 1;
console.log(r);

//SUBTRACTION
r -= 2;
console.log(r);

//MULTIPLICATION
r *= 2;
console.log(r);

//DIVISION
r /= 1;
console.log(r);

//EXPONENTIAL
r **= 2;
console.log(r);

//REMAINDER
r %= 5;
console.log(r);
